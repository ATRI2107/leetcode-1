//   1 0001          1 0001
// + 2 0010         +1 0001
// --------------------------
//   3 0011 = (1^2)  2 0010 (1&1) << 1

//   1 0001     
// + 3 0011
// ------------------
//   2 0010 ^
//  +2 0010 (&)<<1
// ---------------
//   0 0000 ^ 
//  +4 0400 (&)<<1 
      
      
// -2 1110
//  3 0011     
// ---------      
//    1101
//    0100  
// --------
//    1001
//    1000
// --------
//    0001
// (1)0000 truncate the overflowing 1
// -------
//    0001
      
class Solution {
public:
    int getSum(int a, int b) {
        int diff, carry;
        while (b){
            diff = a^b;
            carry = (uint32_t)(a&b)<<1;
            a = diff;
            b = carry;
        }
        return a;
    }
};